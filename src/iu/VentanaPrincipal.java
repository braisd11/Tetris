    //MÉTODOS PARA PAUSAR E QUITAR PAUSA/////////////
    private void contaAtrasPausa (){
        numContaAtras--;
        jLabelContaAtras.setText(numContaAtras+"");
        if (numContaAtras==0){
            quitarPausa();
        }
    }
    
    private void pausar(){
        tqlbtnPausa.setText("START");
        tqlbtnPausa.setSelected(true);
        timer.stop();
        tiempo.stop();
        xogo1.setPausa(true);
        sound.getCancion().stop();
        jLabelContaAtras.setVisible(true);
        jLabelContaAtras.setText(numContaAtras+"");
        juego.setVisible(false);
        panelFichaSeguinte.setVisible(false);
    }
    
    private void quitarPausa(){
        tqlbtnPausa.setText("PAUSE");
        timer.restart();
        tiempo.restart();
        xogo1.setPausa(false);
        jLabelContaAtras.setVisible(false);
        sound.getCancion().loop(ABORT);
        timerContaAtras.stop();
        reiniciarContaAtras();
        tqlbtnPausa.setSelected(false);
        juego.setVisible(true);
        panelFichaSeguinte.setVisible(true);
    }
    //FIN MÉTODOS PARA PAUSAR E QUITAR PAUSA//////////

    
    private void reiniciarContaAtras(){
        numContaAtras=3;
    }
    
