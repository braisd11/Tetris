    //Aumenta a velocidade cada vez que o chaman dende xogo.borrarLinasCompletas()
    public void subirDificultade(){
        int velocidade=100;
        delay=timer.getDelay()- velocidade;
        if(delay<delayMax){
            delay=delayMax;
        }
        timer.setDelay(delay);
    }
    
    //Comproba cantas li√±as se completaron para saber se hai que aumentar a dificultade ou non
    //Cada vez que completa 5 li√±as, chama a VentanaPrincipal.aumentarDificultade().
    public void comprobarLinas(){
        String numerolinas=numlinas.getText();
        int linas=(int) Double.parseDouble(numerolinas);
        if(linas%5==0){
            subirDificultade();
        }
    }
    
    public void aumentarLinas(){
        xogo1.subirChan();
        xogo1.engadirCadradoDificultade();
    }
    
